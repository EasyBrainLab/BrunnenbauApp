/**
 * PLZ (5-stellig) → Bundesland
 * @param {string} zip - 5-stellige PLZ
 * @returns {string|null} Bundesland oder null
 */
function plzToBundesland(zip) {
  if (!zip || zip.length < 2) return null;
  return PLZ_BUNDESLAND[zip.substring(0, 2)] || null;
}

